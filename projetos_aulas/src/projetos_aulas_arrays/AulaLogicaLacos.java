package projetos_aulas_arrays;

import java.util.Scanner;

public class AulaLogicaLacos {

	public static void main(String[] args) {

				
		int pratosLavados = 0;
		
		while (pratosLavados < 10) {
			pratosLavados++;
			if (pratosLavados == 1) {
				System.out.println("Lavei " + (pratosLavados) + " prato.");
			} else {
			System.out.println("Lavei " + (pratosLavados) + " pratos.");}
			}
					
		int coposLavados;
				
		for (coposLavados = 1; coposLavados < 10; coposLavados++) {
						
			if (coposLavados == 1) {
				System.out.println("Lavei " + (coposLavados) + " copo.");
			} else {
			System.out.println("Lavei " + (coposLavados) + " copos.");}
			}
			
		}
	
		
		
		
	}


